@dataclass(frozen=True)
class ArbitrationSample:
    session_id: str
    index: int
    filename: str
    expected: str
    mode: str
    notes: str = ""

    @property
    def relative_path(self) -> Path:
        return Path(self.filename)


class CorpusBenchmarkRegistry:
    def __init__(self, registry: Any, corpus_dir: Path) -> None:
        self.registry = registry
        self.corpus_dir = corpus_dir.resolve()

    @property
    def default_provider_name(self) -> str:
        return getattr(self.registry, "default_provider_name", "vosk")

    def get_provider(self, provider_name: str | None = None) -> Any:
        return self.registry.get_provider(provider_name)

    def resolve_recording_path(self, filename_or_path: str | None) -> Path:
        if not filename_or_path:
            raise AsrProviderError(
                provider="benchmark",
                code="sample_path_missing",
                message="No corpus WAV filename was provided.",
                setup_hint="Add filename entries to local_speech_engine/audio_corpus/labels.json.",
            )

        candidate = Path(filename_or_path)
        if not candidate.is_absolute():
            candidate = self.corpus_dir / candidate

        resolved = candidate.resolve()
        if not resolved.is_relative_to(self.corpus_dir):
            raise AsrProviderError(
                provider="benchmark",
                code="sample_path_outside_corpus",
                message="Benchmark samples must be inside local_speech_engine/audio_corpus.",
                setup_hint="Copy WAV samples into the audio_corpus commands or voice_notes folders.",
                details={"path": str(resolved), "corpus_dir": str(self.corpus_dir)},
            )

        if not resolved.exists() or not resolved.is_file():
            raise AsrProviderError(
                provider="benchmark",
                code="sample_not_found",
                message=f"Corpus WAV sample was not found: {resolved.name}",
                setup_hint="Confirm labels.json filename values are relative to local_speech_engine/audio_corpus.",
                details={"path": str(resolved)},
            )

        if resolved.suffix.lower() != ".wav":
            raise AsrProviderError(
                provider="benchmark",
                code="sample_not_wav",
                message="Arbitration benchmark samples must be .wav files.",
                setup_hint="Use WAV files generated by the local VAD sidecar.",
                details={"path": str(resolved)},
            )

        return resolved


def report_timestamp() -> str:
    return datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")


def parse_provider_names(value: str | Iterable[str] | None) -> list[str]:
    if value is None:
        return ["vosk", "sherpa"]
    if isinstance(value, str):
        names = [name.strip().lower() for name in value.split(",")]
    else:
        names = [str(name).strip().lower() for name in value]
    return [name for name in names if name] or ["vosk", "sherpa"]


def parse_policy_names(value: str | Iterable[str] | None) -> list[str]:
    if value is None:
        return [HYBRID_DEFAULT_POLICY]
    if isinstance(value, str):
        names = [name.strip().lower() for name in value.split(",")]
    else:
        names = [str(name).strip().lower() for name in value]

    normalized_names: list[str] = []
    for name in names:
        if not name:
            continue
        normalized = normalize_policy_name(name)
        if normalized not in normalized_names:
            normalized_names.append(normalized)
    return normalized_names or [HYBRID_DEFAULT_POLICY]


def load_arbitration_samples(labels_path: Path = DEFAULT_LABELS_PATH) -> list[ArbitrationSample]:
    if not labels_path.exists():
        return []

    payload = json.loads(labels_path.read_text(encoding="utf-8") or "[]")
    if isinstance(payload, list):
        return _samples_from_legacy_list(payload)
    if isinstance(payload, dict):
        if isinstance(payload.get("files"), list):
            return _samples_from_session_object(payload)
        if isinstance(payload.get("samples"), list):
            return _samples_from_legacy_list(payload["samples"])
    raise ValueError("Arbitration labels must be a list, an object with files[], or an object with samples[].")


def _samples_from_session_object(payload: dict[str, Any]) -> list[ArbitrationSample]:
    session_id = str(payload.get("session_id") or "unknown_session")
    mode = normalize_profile(payload.get("mode") or COMMAND_PROFILE)
    planned_sequence = [str(item).strip() for item in payload.get("planned_sequence") or []]
    auto_label_by_order = bool(payload.get("auto_label_by_order"))
    samples: list[ArbitrationSample] = []

    for index, file_entry in enumerate(payload.get("files") or []):
        if not isinstance(file_entry, dict):
            continue
        filename = str(file_entry.get("filename") or file_entry.get("file") or "").strip()
        if not filename:
            continue
        expected = str(file_entry.get("expected") or "").strip()
        if not expected and auto_label_by_order and index < len(planned_sequence):
            expected = planned_sequence[index]
        samples.append(
            ArbitrationSample(
                session_id=session_id,
                index=index + 1,
                filename=filename,
                expected=expected,
                mode=normalize_profile(file_entry.get("mode") or mode),
                notes=str(file_entry.get("notes") or "").strip(),
            )
        )

    return samples


def _samples_from_legacy_list(labels: list[Any]) -> list[ArbitrationSample]:
    samples: list[ArbitrationSample] = []
    for index, label in enumerate(labels):
        if not isinstance(label, dict):
            continue
        filename = str(label.get("filename") or label.get("file") or "").strip()
        if not filename:
            continue
        sample_type = str(label.get("type") or "command").strip()
        mode = "voice_note" if sample_type == "voice_note" else COMMAND_PROFILE
        samples.append(
            ArbitrationSample(
                session_id=str(label.get("session_id") or "legacy_labels"),
                index=index + 1,
                filename=filename,
                expected=str(label.get("expected") or "").strip(),
                mode=normalize_profile(label.get("mode") or mode),
                notes=str(label.get("notes") or "").strip(),
            )
        )
    return samples


def run_benchmark(
    *,
    provider_names: str | Iterable[str] | None = None,
    policy_names: str | Iterable[str] | None = None,
    labels_path: Path = DEFAULT_LABELS_PATH,
    corpus_dir: Path = DEFAULT_CORPUS_DIR,
    output_dir: Path = DEFAULT_RESULTS_DIR,
    registry: Any | None = None,
    write_reports: bool = True,
) -> dict[str, Any]:
    samples = load_arbitration_samples(labels_path)
    selected_providers = parse_provider_names(provider_names)
    selected_policies = parse_policy_names(policy_names)
    active_registry = registry or AsrRegistry(
        LocalSpeechConfig.from_env(PROJECT_ROOT, RECORDINGS_DIR)
    )
    arbiter = AsrArbiter(
        CorpusBenchmarkRegistry(active_registry, corpus_dir),
        provider_priority=selected_providers,
    )
    rows: list[dict[str, Any]] = []

    for policy_name in selected_policies:
        for sample in samples:
            rows.extend(_run_sample(
                arbiter=arbiter,
                sample=sample,
                corpus_dir=corpus_dir,
                provider_names=selected_providers,
                policy_name=policy_name,
            ))

    report = build_report(
        rows=rows,
        samples=samples,
        provider_names=selected_providers,
        policy_names=selected_policies,
    )

    if write_reports:
        output_dir.mkdir(parents=True, exist_ok=True)
        timestamp = report["timestamp"]
        json_path = output_dir / f"asr_arbitration_benchmark_{timestamp}.json"
        csv_path = output_dir / f"asr_arbitration_benchmark_{timestamp}.csv"
        report["json_path"] = str(json_path)
        report["csv_path"] = str(csv_path)
        json_path.write_text(json.dumps(report, indent=2, ensure_ascii=True) + "\n", encoding="utf-8")
        _write_csv(csv_path, rows)

    return report


def _run_sample(
    *,
    arbiter: AsrArbiter,
    sample: ArbitrationSample,
    corpus_dir: Path,
    provider_names: list[str],
    policy_name: str,
) -> list[dict[str, Any]]:
    sample_path = (corpus_dir / sample.relative_path).resolve()
    expected = sample.expected
    normalized_expected = _normalize_expected(expected, sample.mode)
    base_row = {
        "session_id": sample.session_id,
        "sample_index": sample.index,
        "filename": sample.filename,
        "mode": sample.mode,
        "policy_name": policy_name,
        "expected": expected,
        "normalized_expected": normalized_expected,
        "notes": sample.notes,
    }

    try:
        result = arbiter.arbitrate_segment(
            filename_or_path=str(sample_path),
            provider_names=provider_names,
            mode=sample.mode,
            policy=policy_name,
        )
    except Exception as error:
        return [{
            **base_row,
            "final_text": "",
            "final_command": "",
            "normalized_final": "",
            "decision_reason": "benchmark_error",
            "arbitration_passed": False,
            "provider": "",
            "provider_raw_transcript": "",
            "provider_normalized_text": "",
            "provider_command": "",
            "provider_confidence": "",
            "provider_latency_ms": "",
            "provider_passed": False,
            "provider_error": str(error),
        }]

    normalized_final = _normalize_result(result.final_command or result.final_text, sample.mode)
    arbitration_passed = bool(normalized_expected and normalized_final == normalized_expected)
    candidates_by_provider = {candidate.provider: candidate for candidate in result.candidates}
    rows: list[dict[str, Any]] = []

    for provider_run in result.provider_runs:
        candidate = candidates_by_provider.get(provider_run.provider)
        provider_normalized = candidate.normalized_text if candidate else ""
        provider_command = candidate.command if candidate and candidate.command else ""
        provider_expected = provider_command or provider_normalized
        provider_passed = bool(normalized_expected and provider_expected == normalized_expected)
        provider_error = ""
        if provider_run.error:
            provider_error = " ".join(
                str(provider_run.error.get(key) or "")
                for key in ("code", "message", "setup_hint")
                if provider_run.error.get(key)
            )

        rows.append({
            **base_row,
            "final_text": result.final_text,
            "final_command": result.final_command or "",
            "normalized_final": normalized_final,
            "decision_reason": result.decision_reason,
            "arbitration_passed": arbitration_passed,
            "provider": provider_run.provider,
            "provider_raw_transcript": provider_run.raw_transcript,
            "provider_normalized_text": provider_normalized,
            "provider_command": provider_command,
            "provider_confidence": provider_run.confidence,
            "provider_latency_ms": provider_run.latency_ms,
            "provider_passed": provider_passed,
            "provider_error": provider_error,
        })

    if not rows:
        rows.append({
            **base_row,
            "final_text": result.final_text,
            "final_command": result.final_command or "",
            "normalized_final": normalized_final,
            "decision_reason": result.decision_reason,
            "arbitration_passed": arbitration_passed,
            "provider": "",
            "provider_raw_transcript": "",
            "provider_normalized_text": "",
            "provider_command": "",
            "provider_confidence": "",
            "provider_latency_ms": "",
            "provider_passed": False,
            "provider_error": "No provider runs returned.",
        })

    return rows


def _normalize_expected(value: str, mode: str) -> str:
    return _normalize_result(value, mode)


def _normalize_result(value: str, mode: str) -> str:
    if normalize_profile(mode) == COMMAND_PROFILE:
        return normalize_asr_text(value, profile=COMMAND_PROFILE)
    return normalize_asr_text(value, profile="voice_note")


def build_report(
    *,
    rows: list[dict[str, Any]],
    samples: list[ArbitrationSample],
    provider_names: list[str],
    policy_names: list[str],
) -> dict[str, Any]:
    sample_keys = {(row["policy_name"], row["session_id"], row["sample_index"]) for row in rows}
    arbitration_passed_keys = {
        (row["policy_name"], row["session_id"], row["sample_index"])
        for row in rows
        if row.get("arbitration_passed")
    }
    return {
        "timestamp": report_timestamp(),
        "providers": provider_names,
        "policies": policy_names,
        "total_samples": len(samples),
        "total_evaluated_samples": len(sample_keys),
        "arbitration": {
            "passed": len(arbitration_passed_keys),
            "total": len(sample_keys),
            "accuracy": _ratio(len(arbitration_passed_keys), len(sample_keys)),
        },
        "by_policy": _policy_summary(rows),
        "by_provider": _provider_summary(rows),
        "by_command": _command_summary(rows),
        "rows": rows,
    }


def _policy_summary(rows: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
    sample_results: dict[tuple[Any, Any, Any], dict[str, Any]] = {}
    for row in rows:
        key = (row.get("policy_name"), row.get("session_id"), row.get("sample_index"))
        sample_results.setdefault(key, row)

    buckets: dict[str, dict[str, Any]] = {}
    for row in sample_results.values():
        policy_name = str(row.get("policy_name") or "unknown")
        bucket = buckets.setdefault(policy_name, {"total": 0, "passed": 0, "accuracy": 0.0})
        bucket["total"] += 1
        if row.get("arbitration_passed"):
            bucket["passed"] += 1

    for bucket in buckets.values():
        bucket["accuracy"] = _ratio(bucket["passed"], bucket["total"])
    return buckets


def _provider_summary(rows: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
    buckets: dict[str, dict[str, Any]] = {}
    for row in rows:
        provider = str(row.get("provider") or "unknown")
        bucket = buckets.setdefault(provider, {"total": 0, "passed": 0, "accuracy": 0.0, "errors": 0})
        bucket["total"] += 1
        if row.get("provider_passed"):
            bucket["passed"] += 1
        if row.get("provider_error"):
            bucket["errors"] += 1

    for bucket in buckets.values():
        bucket["accuracy"] = _ratio(bucket["passed"], bucket["total"])
    return buckets


def _command_summary(rows: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
    sample_results: dict[tuple[Any, Any, Any], dict[str, Any]] = {}
    for row in rows:
        key = (row.get("policy_name"), row.get("session_id"), row.get("sample_index"))
        sample_results.setdefault(key, row)

    buckets: dict[str, dict[str, Any]] = {}
    for row in sample_results.values():
        command = str(row.get("normalized_expected") or row.get("expected") or "unknown")
        bucket = buckets.setdefault(command, {"total": 0, "arbitration_passed": 0, "accuracy": 0.0})
        bucket["total"] += 1
        if row.get("arbitration_passed"):
            bucket["arbitration_passed"] += 1

    for bucket in buckets.values():
        bucket["accuracy"] = _ratio(bucket["arbitration_passed"], bucket["total"])
    return buckets


def _ratio(passed: int, total: int) -> float:
    return (passed / total) if total else 0.0


def _write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    with path.open("w", newline="", encoding="utf-8") as csv_file:
        writer = csv.DictWriter(csv_file, fieldnames=CSV_FIELDS)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row.get(field, "") for field in CSV_FIELDS})


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Benchmark same-WAV ASR arbitration against labeled corpus samples.")
    parser.add_argument("--providers", default="vosk,sherpa", help="Comma-separated sidecar provider names. Default: vosk,sherpa")
    parser.add_argument(
        "--policies",
        default=HYBRID_DEFAULT_POLICY,
        help=(
            "Comma-separated arbitration policies. "
            f"Available: {', '.join(available_policy_names())}. "
            f"Default: {HYBRID_DEFAULT_POLICY}"
        ),
    )
    parser.add_argument("--labels", type=Path, default=DEFAULT_LABELS_PATH, help="Path to arbitration labels.json.")
    parser.add_argument("--corpus-dir", type=Path, default=DEFAULT_CORPUS_DIR, help="Audio corpus root directory.")
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_RESULTS_DIR, help="Directory for JSON/CSV reports.")
    args = parser.parse_args(argv)

    report = run_benchmark(
        provider_names=args.providers,
        policy_names=args.policies,
        labels_path=args.labels,
        corpus_dir=args.corpus_dir,
        output_dir=args.output_dir,
        write_reports=True,
    )
    arbitration = report["arbitration"]
    print(
        "ASR arbitration benchmark complete: "
        f"{arbitration['passed']}/{arbitration['total']} arbitration results passed "
        f"across {report['total_samples']} labeled samples."
    )
    print(f"JSON: {report['json_path']}")
    print(f"CSV: {report['csv_path']}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
